			<h3>Peugeot Stand</h3>
			<p>
				Access360 were commissioned by Wand Agency to produce an eye catching display to promote the Peugeot 3008
				in shopping centres throughout the UK, with the aim to draw attention to the online Spot3008 competition
				where the winner could win a trip in to space.
			</p>

			<h4>Overview</h4>
			<p>
				The stand had various features such as a space themed twinkling floor which also had to take the weight of a real car,
				in addition to illuminated walls and 2 computer stations where members of the public could play the game on the stand.
				Access360 also produced a lifesize Spaceman which people could place their heads inside for a photo opportunity - a real hit with the kids!
			</p>
			<p>
				The stand was fully staffed throughout all of the events together with a team of professional driving instructors to enable visitors
				to have a ride and drive at the venue in one of 5 additional 3008’s supplied by Peugeot.
			</p>
