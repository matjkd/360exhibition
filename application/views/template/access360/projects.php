			<h3>OCR Stand</h3>
			<p>
				Our Client’s brief was to design a stand that would
				create a massive impact and attract as many delegates as
				possible to their stand, in a notoriously competitive
				and forever challenging environment.
			</p>

			<h4>Overview</h4>
			<p>
				The initial stand was designed and created for a 6m x 4m open space, we were
				then challenged to redesign and create a new stand layout that could be installed
				in a 3m x 5m space with 2 closed sides, the redesigned smaller stand being installed
				the week before and with its breakdown on the Saturday it only allowed us a 48 hour
				window to remodel the stand ready for installation at the main event on the Monday.
			</p>
			<p>
				Our design was created to provide the client with a light and inviting design
				that when all the elements of the stand were put together it would flow together
				organically. The stand could be recognised from any point in the hall. Extending
				to 4 metres high, we created a huge light box on the rear of the stand that not
				only looked amazing and attract delegates to the stand, but was also disguising
				the main supporting wall. We were very lucky to work with our clients new metallic branding and
				it was with these metallic finishes we were able to produce a stand that exacted our
				clients requirements. The stand is now being redesigned for a third show.
			</p>
